SELECT t2.JOB_TITLE AS CARGO, CASE
	WHEN t2.MAX_SALARY BETWEEN 5000 AND 10000 THEN 'Baixo'
    WHEN t2.MAX_SALARY BETWEEN 10001 AND 20000 THEN 'Médio'
    WHEN t2.MAX_SALARY BETWEEN 20001 AND 30000 THEN 'Alto'
    WHEN t2.MAX_SALARY > 30000 THEN 'Altíssimo'
    ELSE 'ERRO'
    END
  AS "Nível" FROM employees AS t1
INNER JOIN jobs AS t2
ON t1.JOB_ID = t2.JOB_ID
GROUP BY t1.JOB_ID 
ORDER BY t2.JOB_TITLE;
